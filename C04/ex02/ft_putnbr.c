#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	tw;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb < 10)
		tw = (nb + '0');
	else
	{
		ft_putnbr(nb / 10);
		tw = (nb % 10) + '0';
	}
	write(1, &tw, 1);
}
